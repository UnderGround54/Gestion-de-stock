public class LigneBs {
	
	private int idLigneBs;
	private String numBs;
	private String numProduit;
	private String designProduit;
	private int qteSortie;
	
	
	public int getIdLigneBs() {
		return idLigneBs;
	}
	public void setIdLigneBs(int idLigneBs) {
		this.idLigneBs = idLigneBs;
	}
	public String getNumBs() {
		return numBs;
	}
	public void setNumBs(String numBs) {
		this.numBs = numBs;
	}
	public String getNumProduit() {
		return numProduit;
	}
	public void setNumProduit(String numProduit) {
		this.numProduit = numProduit;
	}
	public int getQteSortie() {
		return qteSortie;
	}
	public void setQteSortie(int qteSortie) {
		this.qteSortie = qteSortie;
	}
	public String getDesignProduit() {
		return designProduit;
	}
	public void setDesignProduit(String designProduit) {
		this.designProduit = designProduit;
	}
}
