package modeleProduit;

/**
 * @author UnderGround
 *
 */
public class Produit {
	private String numProduit;
	private String design;
	private int stock;
	private String dateEntree;
	private String dateSortie;
	private int qteSortie;
	private int qteEntree;
	private String numBs;
	private String numBe;
	
	public String getNumProduit() {
		return numProduit;
	}
	public void setNumProduit(String numProduit) {
		this.numProduit = numProduit;
	}
	public String getDesign() {
		return design;
	}
	public void setDesign(String design) {
		this.design = design;
	}
	public int getStock() {
		return stock;
	}
	public void setStock(int stock) {
		this.stock = stock;
	}
	public String getDateEntree() {
		return dateEntree;
	}
	public void setDateEntree(String dateEntree) {
		this.dateEntree = dateEntree;
	}
	public String getDateSortie() {
		return dateSortie;
	}
	public void setDateSortie(String dateSortie) {
		this.dateSortie = dateSortie;
	}
	public int getQteSortie() {
		return qteSortie;
	}
	public void setQteSortie(int qteSortie) {
		this.qteSortie = qteSortie;
	}
	public int getQteEntree() {
		return qteEntree;
	}
	public void setQteEntree(int qteEntree) {
		this.qteEntree = qteEntree;
	}
	public String getNumBs() {
		return numBs;
	}
	public void setNumBs(String numBs) {
		this.numBs = numBs;
	}
	public String getNumBe() {
		return numBe;
	}
	public void setNumBe(String numBe) {
		this.numBe = numBe;
	}
}
